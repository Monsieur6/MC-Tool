def verifier_webhook(webhook_url):
    return webhook_url.startswith("https://discord.com/api/webhooks/")

def supprimer_webhook():
    try:
        webhook_url = input("\nVeuillez entrer l'URL du Webhook -> ")
        
        if not verifier_webhook(webhook_url):
            print("URL du Webhook invalide.")
            return

        try:
            response = requests.delete(webhook_url)
            response.raise_for_status()
            print("Webhook supprimé avec succès.")
        except requests.exceptions.RequestException as e:
            print(f"Erreur lors de la suppression du Webhook : {e}")
    except Exception as e:
        print(f"Une erreur est survenue : {e}")

if __name__ == "__main__":
    supprimer_webhook()
